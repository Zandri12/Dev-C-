#include <iostream>

using namespace std;

int main()
{
  // deklarasi variabel
  int i;
  double d;
  char c;
  char *s;
  
  // mengisu nilai ke dalam variabel
  i = 88;
  d = 4.675;
  c = 'C';
  s = (char *) "contoh string";
  
  // menampilkan nilai variabel
  cout<<"nilai i: "<<i<<endl;
  cout<<"nilai d: "<<d<<endl;
  cout<<"nilai c: "<<c<<endl;
  cout<<"nilai s: "<<s<<endl;
  
  return 0;
}
